package com.nexenio.rxkeystore;

public class RxKeyStoreException extends Exception {

    public RxKeyStoreException() {
        super();
    }

    public RxKeyStoreException(String message) {
        super(message);
    }

    public RxKeyStoreException(String message, Throwable cause) {
        super(message, cause);
    }

    public RxKeyStoreException(Throwable cause) {
        super(cause);
    }

}
